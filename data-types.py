text = "Hello World!"
print(type(text))

integer = 20
print(type(integer))

floting_data = 20.5
print(type(floting_data))

cmplx = 10j
print(type(cmplx))

# Example of a List
example_list = ["A", "B", "C", "D"]
print(type(example_list))

# Example of a tuple
example_tuple = ("A", "B", "C")
print(type(example_tuple))

# Example of range
example_range = range(6)
print(type(example_range))

# Example of mapping type also called dict
example_map = {"Name": "Tuhin Hossain", "ID": 181472004}
print(type(example_map))

# Example of set
example_set = {"A", "B", "C"}
print(type(example_set))

# Example of Frozenset
example_forzen_set = frozenset({"A", "B", "C"})
print(type(example_forzen_set))

# Example of Boolean Data Type
example_true = True
example_false = False
print(type(example_false))