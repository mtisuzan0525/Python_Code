# Logical Operator
x = 10
print(x > 3 and x < 20)
print(x > 50 or x < 3)
print(not(x > 50 or x < 3))

# Comparison Operators
x = 5
y = 2
print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

# Assignment Operators
x = 5
x += 3
x -= 2
y = 10
y *= 2
print(x)
print(y)

# Arithmetic Operators
x = 3
y = 5
print(x ** y) # x to the power y, x^y
print(y ** x) # y to the power x, y^x

print(x%y)
print(x/y)
print(x*y)
print(x-y)
print(x+y)


